function Encrypt-Shellcode($data, $key) {
    $encrypted = @()
    foreach ($byte in $data) {
        $encrypted += ($byte -bxor $key)
    }
    return $encrypted
}

function Format-Output($name, $data) {
    $out = "unsigned char $name[] = {`n`t"
    $count = 0
    foreach ($byte in $data) {
        $hex = "0x{0:X2}" -f $byte
        $out += "$hex"
        if ($count -lt $data.Count - 1) {
            $out += ", "
        }
        $count++
        if ($count % 12 -eq 0) {
            $out += "`n`t"
        }
    }
    $out += "`n};`n"
    return $out
}

